
package src.srcSecond.domain;

/**
 * Інтерфейс, що визначає методи для відображення результатів у вигляді текстової таблиці.
 * <p>
 * Реалізує пункт 1 завдання: За основу використовувати вихідний текст проекту попередньої 
 * лабораторної роботи. Використовуючи шаблон проектування Factory Method (Virtual Constructor), 
 * розширити ієрархію похідними класами, реалізують методи для подання результатів у вигляді 
 * текстової таблиці.
 * 
 * @author Тесленко Павло
 * @version 1.0
 */
public interface TableView {
    /**
     * Відображає заголовок таблиці
     */
    void displayTableHeader();
    
    /**
     * Відображає тіло таблиці (дані)
     */
    void displayTableBody();
    
    /**
     * Відображає підсумкову частину таблиці
     */
    void displayTableFooter();
    
    /**
     * Відображає всю таблицю (заголовок, тіло, підсумок)
     */
    void displayTable();
    
    /**
     * Встановлює ширину таблиці
     * 
     * @param width ширина таблиці в символах
     * @return встановлена ширина таблиці
     */
    int setTableWidth(int width);
    
    /**
     * Повертає поточну ширину таблиці
     * 
     * @return поточна ширина таблиці в символах
     */
    int getTableWidth();
}
