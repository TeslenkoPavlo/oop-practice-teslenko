
package src;

/**
 * Інтерфейс команди для виконання в черзі завдань.
 * 
 * Є базовим інтерфейсом для всіх команд, що виконуються в системі Worker Thread.
 * Формує основу для реалізації шаблону проектування "Команда" (Command Pattern),
 * що дозволяє інкапсулювати запит як об'єкт, передавати його як параметр,
 * ставити в чергу і виконувати асинхронно.
 * 
 * Всі конкретні команди (пошук мінімуму/максимуму, обчислення середнього, фільтрація)
 * реалізують цей інтерфейс, що забезпечує єдиний спосіб їх запуску через чергу завдань.
 * 
 * @author Павло Тесленко
 * @version 1.0
 */
public interface Command {
    /**
     * Виконує команду.
     * 
     * Цей метод реалізується всіма конкретними командами і містить логіку
     * виконання специфічної операції (наприклад, пошук мінімуму/максимуму,
     * обчислення середнього значення, фільтрація даних тощо).
     * 
     * Викликається робочим потоком (Worker Thread) при обробці елементів з черги.
     */
    void execute();
}
